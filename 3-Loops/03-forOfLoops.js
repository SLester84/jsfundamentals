// For Of Loops

//works well for arrays 

let dogs = ['lab', 'collie', 'biscon' , 'beagle' , 'great dane'];

for (dog of dogs) {
    console.log(dog, 'says woof');
}

// for in loop looks at index

let dogs = ['lab', 'collie', 'biscon' , 'beagle' , 'great dane'];

for (dog in dogs) {
    console.log(dog, 'says woof');
}

// for in gives you the index number
// for of gives you the value in the index number

