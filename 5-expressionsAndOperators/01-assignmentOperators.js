//Assignment Operators

var x = 5; //equal sign is the assignment operatior

//addition Assignments
x += 1; //x =x + 1

//subraction assignment
x -= 1; //x = x - 1

//multiplication assignment
x *= 1; //x = x * 1

//Division assignment
x /= 1; // x = x / 1

//Remainder (or Modulus) assignment
x %= 1; // x = x % 1

//Exponential assignment
x **= 1; // x = x ** 1

