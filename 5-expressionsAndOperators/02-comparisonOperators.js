// Comparison Operators

// equal
'3' == 3;

// strict equal (datatype has to match)
3 === 3;

// Not Equal
'3' != 4;

//Strict not equal (datatype can be different)
'3' !== 3;

//Greater than
3 > 2;

//Less than
2 < 3;

//Less than or equal to
2 <= 3;

//greater than or equal to
3 >= 2;

// And
2 && 2;

//Or
2 || 2;
